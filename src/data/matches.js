export const teams = Array.from(
  new Map(
    matches.flatMap((m) => [
      [m.homeCode, { name: m.home, code: m.homeCode, flag: flags[m.homeCode] || "⚽", group: m.group }],
      [m.awayCode, { name: m.away, code: m.awayCode, flag: flags[m.awayCode] || "⚽", group: m.group }]
    ])
  ).values()
).sort((a, b) => a.name.localeCompare(b.name));

export const groups = Object.values(
  matches.reduce((acc, match) => {
    if (!acc[match.group]) {
      acc[match.group] = { name: match.group, teams: {}, matches: [] };
    }

    acc[match.group].teams[match.homeCode] = {
      name: match.home,
      code: match.homeCode,
      flag: flags[match.homeCode] || "⚽"
    };

    acc[match.group].teams[match.awayCode] = {
      name: match.away,
      code: match.awayCode,
      flag: flags[match.awayCode] || "⚽"
    };

    acc[match.group].matches.push(match);
    return acc;
  }, {})
)
  .map((group) => ({
    ...group,
    teams: Object.values(group.teams).sort((a, b) => a.name.localeCompare(b.name)),
    matches: group.matches.sort((a, b) => new Date(a.kickoffUTC) - new Date(b.kickoffUTC))
  }))
  .sort((a, b) => a.name.localeCompare(b.name));